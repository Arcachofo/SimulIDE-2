#ifndef MCUTYPES_H
#define MCUTYPES_H

#include <inttypes.h>

struct regBits_t{
    uint8_t bit0;
    uint8_t mask;
};

static inline uint8_t getRegBitsVal( uint8_t val, regBits_t rb )
{
    return (val & rb.mask)>>rb.bit0;
}

#endif
